fun main() {
    val year: Short = 1961
    var hour: Byte = 9
    var minute: Byte = 7
    println("Время взлета")
    println(year)
    println(hour)
    println(minute)

    hour = 10
    minute = 55
    println("Время посадки")
    print(hour)
    print(":")
    println(minute)

}